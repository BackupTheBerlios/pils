<p>
  <b>
    what is this
    <a href="/" title="PILS - Python Irc Log Stats">pils</a>
    anyway?
  </b>
  <br />
  it's a tiny <a href="http://www.python.org">python</a>
  script (roughly 2.500 lines currently)
  used to analyze logfiles of irc-conversations and
  generate html-output presenting (more or less useful) statistics
  based on the gathered data.
  <br />
  currently, the code is available only through
  <a href="http://cvs.berlios.de/cgi-bin/viewcvs.cgi/pils/">cvs</a>
  as it is definitely not ready for release yet.
  <br />
  if you're interested and want to help - check it out and drop me a
  <a href="http://developer.berlios.de/users/nikl/">note</a>.
</p>
<p>
  <b>
    what functionality does
    <a href="/" title="PILS - Python Irc Log Stats">pils</a>
    provide?
  </b>
  <br />
  <i>at the moment</i>, it is somewhat limited:
  <ul>
    <li>
      <b>NEW:</b> caching of analyzed data
      - <a href="news.php#20040125">more</a>
    </li>
    <li>
      it only analyzes input files from
      <a href="http://www.dircproxy.net/">dircproxy</a>,
    </li>
    <li>
      there's little to no modularization and
    </li>
    <li>
      you'll find hardly any flexibility at all
      (too many "hard-coded" values).
    </li>
  </ul>
  please be sure to check the <a href="/news.php">news</a>-section for updates
</p>
<p>
  <b>
    what does
    <a href="/" title="PILS - Python Irc Log Stats">pils</a>
    look like?
  </b>
  <br />
  as development has just started there's not much here to see (yet),
  but you may want to check out our linked
  <a href="/examples.php">examples</a>.
</p>
